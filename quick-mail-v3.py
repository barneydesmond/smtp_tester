"""
Uses standard python libraries to make an email and send it.

This is the third revision of this script, we now attach a bunch of MIME
attachments to make the mail more interesting and structured. No HTML or
anything fancy, but it should look decent in your mail client.

Written targeting Python 2, sometime in 2008. <barney@anchor.net.au>
Updated to Python 3 in 2022. <barneydesmond@gmail.com>
"""

import smtplib
import datetime
from argparse import ArgumentParser

from email.mime.application import MIMEApplication
from email.mime.image import MIMEImage
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText

parser = ArgumentParser()
parser.add_argument(
    "-n", type=int, dest="howmany", default=1, help="Specify how many messages to send"
)
parser.add_argument(
    "--from",
    dest="fromaddr",
    default="barney@anchor.net.au",
    help="What email address should the message appear to come from",
)
parser.add_argument(
    "--to",
    dest="toaddr",
    default="johnny@example.com",
    help="Who should the email be addressed to",
)
parser.add_argument(
    "--host",
    "--server",
    dest="server",
    default="localhost",
    help="Specify the SMTP server to connect to",
)
parser.add_argument(
    "-p",
    "--port",
    type=int,
    dest="port",
    default=25,
    help="What port should the SMTP server be contacted on",
)
args = parser.parse_args()

TIMESTAMP = datetime.datetime.now().ctime()
FROM = args.fromaddr
TO = args.toaddr
SMTP_SERVER = args.server
SMTP_PORT = args.port


